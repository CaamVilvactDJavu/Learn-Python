import turtle

# Square
qazi_turtle = turtle.Turtle()


def square():
    qazi_turtle.forward(100)
    qazi_turtle.right(90)
    qazi_turtle.forward(100)
    qazi_turtle.right(90)
    qazi_turtle.forward(100)
    qazi_turtle.right(90)
    qazi_turtle.forward(100)


square()
qazi_turtle.forward(200)
